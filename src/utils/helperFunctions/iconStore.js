import { cloneElement } from "react";
import { twMerge } from "tailwind-merge";

// Wrapper component to merge classNames without conflict.
const withMerger = (Icon) => (props) => {
  const { className = "", title, ...rest } = props;
  const mergedClassName = twMerge(Icon().props?.className, className);

  // Render the icon and inject the <title> tag dynamically
  const iconElement = Icon({ className: mergedClassName, ...rest });

  return cloneElement(
    iconElement,
    {},
    <>
      {title && <title>{title}</title>}
      {iconElement.props.children} {/* Preserve original SVG content */}
    </>
  );
};

